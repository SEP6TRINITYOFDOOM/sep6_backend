package com.sep6.app.service.DTO;

public class MovieCredits {
    public MovieCredits(){}
    Cast[] cast;
    Crew[] crew;
    int id;

    public Cast[] getCast() {
        return cast;
    }

    public void setCast(Cast[] cast) {
        this.cast = cast;
    }

    public Crew[] getCrew() {
        return crew;
    }

    public void setCrew(Crew[] crew) {
        this.crew = crew;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
}
